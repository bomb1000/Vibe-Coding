  function normalizeLanguage(language) {
    if (!language || language === 'auto') return 'auto';
    if (language.toLowerCase().startsWith('zh')) return 'zh_TW';
    if (language.toLowerCase().startsWith('en')) return 'en';
    if (language.toLowerCase().startsWith('ja')) return 'ja';
    if (language.toLowerCase().startsWith('ko')) return 'ko';
    if (language.toLowerCase().startsWith('es')) return 'es';
    return DEFAULT_LANGUAGE;
  }

  function detectLanguage() {
    const chromeLanguage = chrome.i18n?.getUILanguage ? chrome.i18n.getUILanguage() : navigator.language;
    return normalizeLanguage(chromeLanguage) === 'auto' ? DEFAULT_LANGUAGE : normalizeLanguage(chromeLanguage);
  }

  function getStoredLanguage() {
    return new Promise(resolve => {
      if (!chrome.storage?.sync) {
        resolve(detectLanguage());
        return;
      }
      chrome.storage.sync.get([STORAGE_KEY], result => {
        const language = normalizeLanguage(result[STORAGE_KEY] || 'auto');
        resolve(language === 'auto' ? detectLanguage() : language);
      });
    });
  }

  function getRawStoredLanguage() {
    return new Promise(resolve => {
      if (!chrome.storage?.sync) {
        resolve('auto');
        return;
      }
      chrome.storage.sync.get([STORAGE_KEY], result => {
        resolve(normalizeLanguage(result[STORAGE_KEY] || 'auto'));
      });
    });
  }

  function setLanguage(language) {
    return new Promise((resolve, reject) => {
      if (!chrome.storage?.sync) {
        resolve();
        return;
      }
      chrome.storage.sync.set({ [STORAGE_KEY]: normalizeLanguage(language) }, () => {
        if (chrome.runtime.lastError) {
          reject(new Error(chrome.runtime.lastError.message));
        } else {
          resolve();
        }
      });
    });
  }

  function translate(key, language) {
    const lang = normalizeLanguage(language) === 'auto' ? detectLanguage() : normalizeLanguage(language);
    return MESSAGES[lang]?.[key] || MESSAGES[DEFAULT_LANGUAGE][key] || key;
  }

  async function t(key) {
    const language = await getStoredLanguage();
    return translate(key, language);
  }

  async function applyDocument() {
    const language = await getStoredLanguage();
    document.documentElement.lang = language.replace('_', '-');
    document.querySelectorAll('[data-i18n]').forEach(element => {
      element.textContent = translate(element.dataset.i18n, language);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
      element.setAttribute('placeholder', translate(element.dataset.i18nPlaceholder, language));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
      element.setAttribute('title', translate(element.dataset.i18nTitle, language));
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
      element.setAttribute('aria-label', translate(element.dataset.i18nAriaLabel, language));
    });
    const titleKey = document.documentElement.dataset.i18nTitleKey;
    if (titleKey) document.title = translate(titleKey, language);
    return language;
  }

  globalThis.EWI18n = {
    applyDocument,
    detectLanguage,
    getRawStoredLanguage,
    getStoredLanguage,
    setLanguage,
    t,
    translate
  };
